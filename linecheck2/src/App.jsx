function normTok(t) {
  // Strip leading # (for #10 cans)
  t = t.replace(/^#/,'');
  return SYNONYMS[t] || t;
}

function tok(s) {
  return s.toLowerCase()
    .replace(/[^a-z0-9#]/g,' ')
    .split(/\s+/)
    .map(t=>t.replace(/^#/,''))           // strip # from #10
    .map(t=>SYNONYMS[t]||t)               // apply synonyms
    .filter(t=>t.length>1&&!STOP.has(t)); // filter stop words
}
function jacc(a, b) {
  const A=new Set(tok(a)), B=new Set(tok(b));
  if(!A.size&&!B.size) return 0;
  const inter=[...A].filter(t=>B.has(t)).length;
  return inter/(A.size+B.size-inter);
}
function buildMatches(l1, l2) {
  const used=new Set();
  return l1.map((x,i)=>{
    let bi=-1,bs=0;
    l2.forEach((y,j)=>{ if(used.has(j))return; const s=jacc(x.name,y.name); if(s>bs){bs=s;bi=j;} });
    if(bs>0.2&&bi>=0) used.add(bi);
    return {id:i, d1:x, d2:bs>0.2&&bi>=0?l2[bi]:null, sc:bs};
  });
}

