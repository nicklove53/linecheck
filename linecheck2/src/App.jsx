function detectMap(headers, sampleRows) {
  const h=headers.map(s=>s.toLowerCase().replace(/[^a-z0-9\s#]/g,' '));
  const pick=kws=>{ for(const k of kws){ const i=h.findIndex(c=>c.includes(k)); if(i>=0)return headers[i]; } return null; };

  const map = {
    name:  pick(['desc','name','item','product','mat']),
    price: pick(['price','cost','each','net','amount','rate']),
    sku:   pick(['sku','item #','item#','code','num','#','prod']),
    uom:   pick(['uom','unit of','unit m','unit']),
    pack:  pick(['package size','pack size','pack','package','size']),
  };

  // Data-driven fallback — scan actual row values when keywords don't match
  if(sampleRows && sampleRows.length && (!map.name || !map.price)) {
    const sample = sampleRows.slice(0, 10);
    const scores = headers.map(h => ({
      h,
      priceScore: sample.filter(r => /^\$?\d{1,4}\.\d{2}$/.test(String(r[h]||'').trim())).length,
      nameScore:  sample.filter(r => { const v=String(r[h]||'').trim(); return v.length>6 && isNaN(v) && !/^\$/.test(v); }).length,
      skuScore:   sample.filter(r => /^\d{5,10}$/.test(String(r[h]||'').trim())).length,
      packScore:  sample.filter(r => /\d+[\/]\d+|\d+\s*(lb|oz|ga|ea)/i.test(String(r[h]||'').trim())).length,
    }));

    if(!map.price) {
      const best = scores.sort((a,b)=>b.priceScore-a.priceScore)[0];
      if(best.priceScore > 0) map.price = best.h;
    }
    if(!map.name) {
      const best = scores.sort((a,b)=>b.nameScore-a.nameScore)[0];
      if(best.nameScore > 0 && best.h !== map.price) map.name = best.h;
    }
    if(!map.sku) {
      const best = scores.sort((a,b)=>b.skuScore-a.skuScore)[0];
      if(best.skuScore > 0 && best.h !== map.price && best.h !== map.name) map.sku = best.h;
    }
    if(!map.pack) {
      const best = scores.sort((a,b)=>b.packScore-a.packScore)[0];
      if(best.packScore > 0 && best.h !== map.price && best.h !== map.name) map.pack = best.h;
    }
  }

  return map;
}
function parseRows(rows, map) {
  return rows.filter(r=>r[map.name]&&r[map.price]).map(r=>({
    name:  String(r[map.name]).trim(),
    sku:   map.sku  ? String(r[map.sku]||'').trim() : '',
    price: parseFloat(String(r[map.price]).replace(/[^0-9.]/g,''))||0,
    uom:   map.uom  ? String(r[map.uom]||'CS').trim() : 'CS',
    pack:  map.pack ? String(r[map.pack]||'').trim() : '',
  })).filter(r=>r.name&&r.price>0);
}


// ── Pack Size & Unit Price Engine ─────────────────────────────────────────────
function normalizeUnit(raw) {
  let u = String(raw).trim().toUpperCase();
  // Explicit suffix normalizations first
  u = u.replace(/^LBA(V?)$/, 'LB');   // LBAV, LBA → LB
  u = u.replace(/^LBS$/, 'LB');
  u = u.replace(/^GALLON$/, 'GAL');
  u = u.replace(/^QUART(S?)$/, 'QT');
  u = u.replace(/^EACH$/, 'EA');
  u = u.replace(/^P(C|CS|K|KG|KT)$/, 'PK');
  u = u.replace(/^CAN$/, 'EA');
  // Map known aliases
  if(['GA','GL','GAL'].includes(u)) return 'GAL';
  if(['LB'].includes(u))            return 'LB';
  if(['OZ'].includes(u))            return 'OZ';
  if(['QT'].includes(u))            return 'QT';
  if(['EA','PK','CT'].includes(u))  return 'EA';
  if(['RL','ROLL'].includes(u))     return 'RL';
  return u;
}

function toOz(qty, unit) {
  if(unit==='LB')  return qty * 16;
  if(unit==='OZ')  return qty;
  if(unit==='GAL') return qty * 128;
  if(unit==='QT')  return qty * 32;
  return null;
}

function parsePack(str) {
  if(!str) return null;
  const s = String(str).toUpperCase().trim();
  // Pattern 1: N/X/UNIT or N/X UNIT
  let m = s.match(/^(\d+)\s*\/\s*(\d*\.?\d+)\s*\/?([A-Z]+)/);
  if(m) {
    const count=parseFloat(m[1]), each=parseFloat(m[2]);
    const unit=normalizeUnit(m[3]);
    const totalInUnit=count*each;
    const totalOz=toOz(totalInUnit, unit);
    return {count,each,unit,totalInUnit,totalOz,totalLb:totalOz?totalOz/16:null};
  }
  // Pattern 2: N UNIT — e.g. "50 LB", "2.5 GA", "48 EA"
  m = s.match(/^(\d+\.?\d*)\s+([A-Z]+)/);
  if(m) {
    const total=parseFloat(m[1]), unit=normalizeUnit(m[2]);
    const totalOz=toOz(total, unit);
    return {count:1,each:total,unit,totalInUnit:total,totalOz,totalLb:totalOz?totalOz/16:null};
  }
  return null;
}

function unitPrice(price, uom, pack) {
  const u = normalizeUnit((uom||'CS').toUpperCase());

  // Already priced per unit — return as-is in native unit
  if(u==='LB')  return {val:price,              unit:'lb'};
  if(u==='OZ')  return {val:price,              unit:'oz'};  // keep as oz, cross-convert later
  if(u==='GAL') return {val:price,              unit:'gal'};
  if(u==='QT')  return {val:price/4,            unit:'gal'};
  if(u==='EA')  return {val:price/(pack?.totalInUnit||1), unit:'ea'};

  // Priced per case — normalize by pack type (GAL before weight!)
  if(pack?.unit==='GAL')
    return {val:price/(pack.totalInUnit||1),     unit:'gal'};
  if(pack?.unit==='QT')
    return {val:price/((pack.totalInUnit||4)/4), unit:'gal'};
  if(pack?.unit==='OZ' && pack?.totalOz)
    return {val:price/pack.totalOz,              unit:'oz'};  // keep as oz
  if(pack?.unit==='EA')
    return {val:price/(pack.totalInUnit||1),     unit:'ea'};
  if(pack?.unit==='RL')
    return {val:price/(pack.totalInUnit||1),     unit:'ea'};
  if(pack?.totalLb)
    return {val:price/pack.totalLb,              unit:'lb'};
  if(pack?.totalOz)
    return {val:price/pack.totalOz,              unit:'oz'};  // fallback: oz
  if(pack?.count&&pack.count>1)
    return {val:price/pack.count,                unit:'ea'};
  return null;
}

