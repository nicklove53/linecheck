// ── Auth View (Login + Signup) ────────────────────────────────────────────────
function AuthView({ mode, C, onAuth, onSwitch, onBack }) {
  const [email,    setEmail]    = useState('');
  const [password, setPassword] = useState('');
  const [name,     setName]     = useState('');
  const [plan,     setPlan]     = useState('operator');
  const [error,    setError]    = useState('');
  const [loading,  setLoading]  = useState(false);

  const isSignup = mode === 'signup';
  const inp = { width:'100%', background:'#F9F9F9', border:`1px solid ${C.bdr}`, borderRadius:8, padding:'11px 14px', fontSize:14, color:C.txt, outline:'none', boxSizing:'border-box', marginBottom:12 };

  const handle = async () => {
    if(!email||!password||(isSignup&&!name)) { setError('Please fill in all fields.'); return; }
    if(password.length<6) { setError('Password must be at least 6 characters.'); return; }
    setLoading(true); setError('');
    const err = isSignup ? await onAuth(email, password, name, plan) : await onAuth(email, password);
    setLoading(false);
    if(err) setError(err);
  };

  return (
    <div style={{background:C.bg, minHeight:'100vh', fontFamily:"system-ui,-apple-system,sans-serif", color:C.txt, display:'flex', alignItems:'center', justifyContent:'center', padding:24}}>
      <div style={{width:'100%', maxWidth:420}}>
        {/* Logo */}
        <div style={{textAlign:'center', marginBottom:32}}>
          <div onClick={onBack} style={{color:C.acc,fontWeight:900,fontSize:26,letterSpacing:-1,cursor:'pointer',marginBottom:6}}>Linecheck</div>
          <div style={{color:C.sub,fontSize:14}}>{isSignup?'Create your account':'Welcome back'}</div>
        </div>

        <div style={{background:C.surf,border:`1px solid ${C.bdr}`,borderRadius:14,padding:28,boxShadow:'0 2px 16px rgba(0,0,0,0.06)'}}>
          {isSignup&&(
            <input value={name} onChange={e=>setName(e.target.value)} placeholder="Your name" style={inp}/>
          )}
          <input type="email" value={email} onChange={e=>setEmail(e.target.value)} placeholder="Email address" style={inp}/>
          <input type="password" value={password} onChange={e=>setPassword(e.target.value)} placeholder="Password (min 6 characters)"
            style={{...inp,marginBottom:isSignup?16:20}}
            onKeyDown={e=>e.key==='Enter'&&handle()}/>

          {isSignup&&(
            <div style={{marginBottom:20}}>
              <div style={{fontSize:12,fontWeight:700,color:C.sub,textTransform:'uppercase',letterSpacing:1,marginBottom:10}}>Choose your plan</div>
              <div style={{display:'grid',gridTemplateColumns:'1fr 1fr',gap:8}}>
                {[
                  {key:'operator', icon:'🍽️', title:'Operator', price:'$50/mo', sub:'Restaurant owners comparing distributor price lists'},
                  {key:'rep',      icon:'💼', title:'Sales Rep', price:'$200/mo', sub:'Reps showing clients how much they can save'},
                ].map(({key,icon,title,price,sub})=>(
                  <div key={key} onClick={()=>setPlan(key)} style={{
                    border:`2px solid ${plan===key?C.acc:C.bdr}`,borderRadius:10,padding:'12px 14px',cursor:'pointer',
                    background:plan===key?C.grnBg:'transparent',transition:'all .15s'
                  }}>
                    <div style={{fontSize:18,marginBottom:4}}>{icon}</div>
                    <div style={{fontWeight:700,fontSize:13,color:plan===key?C.acc:C.txt}}>{title}</div>
                    <div style={{fontSize:12,color:C.acc,fontWeight:700,marginBottom:3}}>{price}</div>
                    <div style={{fontSize:11,color:C.sub,lineHeight:1.4}}>{sub}</div>
                  </div>
                ))}
              </div>
              <div style={{fontSize:11,color:C.sub,marginTop:8,textAlign:'center'}}>
                ✓ 3 free comparisons to start — no credit card required
              </div>
            </div>
          )}

          {error&&<div style={{background:'#FFF0F0',border:'1px solid #FFCCCC',borderRadius:7,padding:'9px 12px',fontSize:13,color:'#CC0000',marginBottom:14}}>{error}</div>}

          <button onClick={handle} disabled={loading} style={{
            width:'100%',background:C.acc,color:'#fff',border:'none',borderRadius:8,
            padding:13,fontWeight:700,fontSize:15,cursor:loading?'not-allowed':'pointer',
            opacity:loading?0.7:1
          }}>
            {loading?'Please wait...':(isSignup?'Create Account & Start Free Trial':'Sign In')}
          </button>

          <div style={{textAlign:'center',marginTop:16,fontSize:13,color:C.sub}}>
            {isSignup?'Already have an account? ':'New to Linecheck? '}
            <span onClick={onSwitch} style={{color:C.acc,fontWeight:700,cursor:'pointer'}}>
              {isSignup?'Sign in':'Create account'}
            </span>
          </div>
        </div>
        <div style={{textAlign:'center',marginTop:16,fontSize:11,color:C.sub}}>
          Your data never leaves your device &nbsp;·&nbsp; No credit card to try it
        </div>
      </div>
    </div>
  );
}

