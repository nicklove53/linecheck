// Reconcile two unit prices to a common unit if possible
function reconcileUnits(u1, u2) {
  if(u1.unit===u2.unit) return {p1:u1.val, p2:u2.val, unit:u1.unit, converted:false};
  // Weight: oz ↔ lb  (16 oz per lb)
  if((u1.unit==='oz'&&u2.unit==='lb')||(u1.unit==='lb'&&u2.unit==='oz')) {
    const p1oz=u1.unit==='lb'?u1.val/16:u1.val;
    const p2oz=u2.unit==='lb'?u2.val/16:u2.val;
    return {p1:p1oz, p2:p2oz, unit:'oz', converted:true};
  }
  // Volume: oz ↔ gal  (128 fl oz per gal)
  if((u1.unit==='oz'&&u2.unit==='gal')||(u1.unit==='gal'&&u2.unit==='oz')) {
    const p1oz=u1.unit==='gal'?u1.val/128:u1.val;
    const p2oz=u2.unit==='gal'?u2.val/128:u2.val;
    return {p1:p1oz, p2:p2oz, unit:'oz', converted:true};
  }
  // Volume: qt ↔ oz  (32 fl oz per qt)
  if((u1.unit==='qt'&&u2.unit==='oz')||(u1.unit==='oz'&&u2.unit==='qt')) {
    const p1oz=u1.unit==='qt'?u1.val/32:u1.val;
    const p2oz=u2.unit==='qt'?u2.val/32:u2.val;
    return {p1:p1oz, p2:p2oz, unit:'oz', converted:true};
  }
  // ea ↔ ea (already same, handled above)
  return null; // truly incompatible (e.g. lb vs gal)
}

// Extract pack size from item name if pack field is empty
// e.g. "CHICKEN BREAST(6/5 LB)" → parsePack("6/5 LB")
function extractPackFromName(item) {
  if(!item) return null;
  // Try explicit pack field first
  if(item.pack && item.pack.trim()) return parsePack(item.pack);
  // Fall back: look for pack pattern in parentheses in name
  const parenMatch = (item.name||'').match(/\(([^)]+)\)\s*$/);
  if(parenMatch) {
    const p = parsePack(parenMatch[1]);
    if(p) return p;
  }
  // Fall back: look for pack pattern anywhere in name
  const inlineMatch = (item.name||'').match(/(\d+\/\.?\d+\s*\/?[A-Z]+|\d+\.?\d*\s+[A-Z]+)\s*$/i);
  if(inlineMatch) return parsePack(inlineMatch[1]);
  return null;
}

function compareUnitPrices(m, rb1, rb2, netP) {
  if(!m.d1||!m.d2) return null;
  const p1=netP(m.d1.price,rb1), p2=netP(m.d2.price,rb2);
  const pack1=extractPackFromName(m.d1), pack2=extractPackFromName(m.d2);
  const identical=packsIdentical(pack1,pack2);
  const u1=unitPrice(p1,m.d1.uom,pack1);
  const u2=unitPrice(p2,m.d2.uom,pack2);
  if(!u1||!u2) return {status:'incompatible',p1:null,p2:null,unit:null,identical:false};

  const rec=reconcileUnits(u1,u2);
  if(!rec) return {status:'incompatible',p1:null,p2:null,unit:null,identical:false};

  let status='ok';
  if(rec.converted) status='converted';
  else if(!identical && pack1&&pack2) status=pack1.unit===pack2.unit?'diff-qty':'converted';
  return {status, p1:rec.p1, p2:rec.p2, unit:rec.unit, identical};
}

